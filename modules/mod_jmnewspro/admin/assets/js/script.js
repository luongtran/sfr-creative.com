function jmnewspro_show_image(option){
    if(option){
        jQuery('#jform_params_jmnewspro_image_style').parents('li').css({
            display:'block'
        });
        jQuery('#jform_params_jmnewspro_image_link').parent().css({
            display:'block'
        });
    }else{
        jQuery('#jform_params_jmnewspro_image_style').parents('li').css({
            display:'none'
        });
        jQuery('#jform_params_jmnewspro_image_link').parent().css({
            display:'none'
        });
    }
}
function jmnewspro_AutoChange(source){
    switch(source){
        case 'true':
            jQuery('#jform_params_jmnewspro_timeout').parent().css({display:'block'});
            break;
        case 'false':
            jQuery('#jform_params_jmnewspro_timeout').parent().css({display:'none'});
            break;
    }
}
function jmnewspro_titlelink(option){
    if(option == 1){
        jQuery('#jform_params_jmnewspro_title_link').parent().css('display','block');
    }else{
        jQuery('#jform_params_jmnewspro_title_link').parent().css('display','none');
    }
}